class ShoppingCart:
    def __init__(self, inventory):
        # Available items in the store
        self.inventory = inventory
        # Customer's cart
        self.cart = {}

    def display_inventory(self):
        """Display available items in the store."""
        print("\nüõí Available Items in Inventory:")
        for name, price in self.inventory.items():
            print(f" - {name}: ‚Çπ{price:.2f}")

    def display_cart(self):
        """Display items in the user's cart."""
        if not self.cart:
            print("\nüõçÔ∏è Your cart is empty.")
        else:
            print("\nüõçÔ∏è Items in your Cart:")
            for name, price in self.cart.items():
                print(f" - {name}: ‚Çπ{price:.2f}")
            print(f"\nüí∞ Total Bill: ‚Çπ{self.total_cost():.2f}")

    def add_item(self, name):
        """Add an item to the cart if it exists in inventory."""
        if name in self.inventory:
            self.cart[name] = self.inventory[name]
            print(f"‚úÖ {name} added to your cart for ‚Çπ{self.inventory[name]:.2f}")
        else:
            print(f"‚ö†Ô∏è {name} not found in inventory!")

    def remove_item(self, name):
        """Remove an item from the cart."""
        if name in self.cart:
            del self.cart[name]
            print(f"üóëÔ∏è {name} removed from your cart.")
        else:
            print(f"‚ö†Ô∏è {name} not found in your cart!")

    def total_cost(self):
        """Return the total cost of items in the cart."""
        return sum(self.cart.values())


